package edu.bsuc.entity;

public class Institute {
	
	/**主键id*/
	private Integer id;
	
	/**学院名称*/
	private String name;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	
}
